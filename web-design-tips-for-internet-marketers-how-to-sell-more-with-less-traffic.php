                                    <h3>3.How well does your website listen to the visitors ?
                                        Empathise</h3>

                                    <p>

                                        We humans like to talk to people who are worthy of our attention. If a person
                                        talks about himself always, we are put off. Does he like to listen to you ?

                                        Websites are never as good as humans in empathizing with customer's
                                        requirement.
                                        Your website needs to learn from your sales people to do that. The more the
                                        website knows about the customer, better engaging it becomes.

                                        Customers are looking for something that meets their requirements and at the
                                        same time within their own spending capacity. Make it easy for him to make that
                                        decision with least effort.

                                        Answer the frequently asked questions without even being asked. Simplify the
                                        product offering using images videos and only limited text. Make it
                                        entertaining. Connect emotionally with the customer, help him to solve the
                                        problem the customer is trying to solve with your help.

                                        If properly implemented, your website can sell better than your best sales
                                        person. Even your best sales person gets tired at the end of the day. The
                                        website doesn't !

                                        How intuitive is your user experience ?

                                        The user experience of a site plays a vital role in sales. Make sure key
                                        elements of the website are catchy. Spend attention to positioning of images,
                                        buttons and text to make sure that they convey their purpose clearly.

                                        Be Context Sensitive

                                        When a user is visiting a particular page, try to give glimpses of the
                                        information he is likely to think of in the same page. Do not bury that
                                        information deep within menus.

                                        Proper use of anchor texts, improves readability. The anchor texts are the
                                        clickable text in hyperlink which enables navigation to other pages. Never let
                                        your clients get tangled inside your site with complex navigation design.


                                    </p>
                                    <h3> 4.Understand power of emotions</h3>

                                    <p>Emotions are the shortest path to trigger a successful business. Before that we
                                        need to figure out what appeals to our target audience to spend more time on our
                                        site. Try to put ourselves in customer's shoe and import their emotion as
                                        ours.
                                        Thus we should ensure our trust.

                                        Build Credibility from the beginning.

                                        Display your best work with pride. List out your top customer's in the home page
                                        itself. This gives the visitor a lot of confidence. Remember, most of the
                                        traffic at the home page may not make it to the remaining pages.

                                        Are you on the expensive side ? Explain Why

                                        Let us consider a visitor, who is looking for an affordable home. If the site
                                        says we only deal with luxury apartments, he will leave immediately. If the
                                        website does a good job of detailing the features we offer at the extra cost, he
                                        may change his mind.</p>


