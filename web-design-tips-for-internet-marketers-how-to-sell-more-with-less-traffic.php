                                    <h3>5.Chat Interfaces and Inquiry Forms</h3>

                                    <p>
                                        Chat Interfaces

                                        Chat windows bring in the human element. Make sure a qualified sales person
                                        answers the questions prospects have. When they are not able to address any
                                        queries, get the prospect's contact details and get back to them.

                                        It is very easy to convert a prospect who has decided to chat with you.

                                        Inquiry Forms  For god's sake ask contact number.

                                        It is very easy to enter phone number. Sometimes, easier than typing your own
                                        name.

                                        Enquiry forms should be easily accessible. These forms should have least number
                                        of mandatory fields. The information that you would like to have, but not
                                        required can move to a second non-mandatory form. If you know how to contact
                                        your customer, it is easier to call him up and get the remaining information.
                                    </p>

