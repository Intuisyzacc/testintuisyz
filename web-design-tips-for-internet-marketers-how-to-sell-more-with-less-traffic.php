                                    <h3>2.Engaging the Prospects Online</h3>

                                    <p>
                                        Just like a smart sales person engages the prospect in any shop personally, your
                                        website needs to engage surfers browsing through your website. The time they
                                        spend in your website is an indicator of how well the website is engaging with
                                        the prospects.

                                        Bounce Rate

                                        Bounce rate is the measure of the number of people who leave your website before
                                        the first page gets loaded. It is the percentage of visitors who enter the site
                                        and then leave without continuing to view other pages within the same site.
                                        Bounce rates helps in finding out the effectiveness or performance of an entry
                                        page on generating interest to visitors in adhering to the site. Lesser bounce
                                        rate denotes better website, which can make visitors to cling on to the site.
                                        Factors affecting bounce rate are :



                                    </p>
