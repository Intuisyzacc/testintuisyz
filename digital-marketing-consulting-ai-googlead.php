    // Function to generate a captcha string
    function generateCaptcha(length) {
        var characters = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789';
        var captchaString = '';
        for (var i = 0; i < length; i++) {
            var randomIndex = Math.floor(Math.random() * characters.length);
            captchaString += characters.charAt(randomIndex);
        }
        return captchaString;
    }

    // Generate captcha and display it in the designated element
    var captcha = generateCaptcha(6); // Generates a 6-character captcha string
    $('#captchaElement').text(captcha); // Display the generated captcha in the specified element

    // Function to validate the captcha
    function validateCaptcha() {
        var userEnteredCaptcha = $('#captcha').val();
        var generatedCaptcha = $('#captchaElement').text(); // Get the actual generated captcha from the element

        if (userEnteredCaptcha === generatedCaptcha) {
            $('#captchaError').text(''); // Clear captcha error message
            return true;
        } else {
            var confirmation = confirm('Incorrect captcha. Do you want to try again?');
            if (confirmation) {
                // Clear captcha field
                $('#captcha').val('');
                // Regenerate captcha
                var newCaptcha = generateCaptcha(6);
                $('#captchaElement').text(newCaptcha); // Display the new captcha
            }
            return false;
        }
    }

    // Attach form submission event handler
    $('#cont').submit(function(event) {
        event.preventDefault(); // Prevent default form submission behavior

        // Validate the captcha
        if (validateCaptcha()) {
            // Perform form submission
            $.ajax({
                type: 'POST',
                url: 'consultingdb.php',
                data: $(this).serialize(),
                success: function(response) {
                    // Display success message
                    alert('Form submitted successfully!');
                    // Reload the page
                    location.reload();
                },
                error: function(xhr, status, error) {
                    console.error('Form submission error:', error);
                }
            });
        }
    });
