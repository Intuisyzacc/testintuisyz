                                <div class="info">
                                    <p>Every human being is an emotional fool to some extent.Emotions are far more
                                        powerful than reason in humans-even the reasonable ones.For decades marketing
                                        professionals have tricked masses using this susceptibility.

                                        Perfumes to soda brands use scantly clad models to sell their products.Fraction
                                        of a second is enough for a potential mate to catch any animal's attention,
                                        humans are not much different.Television advertisement spots cost a fortune for
                                        a second, so they succeed in pitching their products very cost effectively using
                                        this knowledge.Digital marketing companies have started using emotions far more
                                        effectively than conventional marketers. Coupled with machine learning, social
                                        media marketing firms are dangerously close to manipulating public opinion and
                                        behavior is a scale unimaginable for us.</p>

                                    <h3>Power of emotions over reason - The rationale behind !</h3>
                                    <p>Like every other living organism, humans are driven by two primary impulses,
                                        Survival and reproduction. When you are given a choice between them, animals
                                        always choose survival over reproduction. Nature is very rational, if you
                                        survive today, you can reproduce tomorrow. This is the basic reason why fear is
                                        more powerful than almost any emotion. A healthy fear of predators is necessary
                                        for a species to survive.

                                        Among these two basic impulses, social media has succeeded far better with use
                                        of fear than any other emotion. The reason is the new magic word of sharing.
                                        Most animals have a mechanism to signal information of danger to it's tribe.
                                        This mechanism is wired into human brains through millions of years of
                                        evolution. When we perceive a danger to our tribe, we have an instinct to share
                                        with others. There is no time to analyse, we pass on the information almost like
                                        a reflex.

                                        No matter how much you liked an advertisement on TV, you had no option to share
                                        it with your friends. Internet has turned the tables on traditional marketers -
                                        if you can engage, you get free spots of social media advertisement. If you can
                                        get people to share your message, you save a lot of money on media buying</p>
                                    <div class="thumb">
                                        <a href="#"><img src="/images/tv-ad-Internet-ads.jpg" style="width: 100%;"
                                                alt="Thumb"></a>
                                    </div>
                                    <p>Even when you are enraged by a divisive message and angrily reply to a politician
                                        you hate, you are unwittingly sharing the message with your friends. At least
                                        some of you friends are getting notification that you responded. The politician
                                        gets more publicity at your expense.

                                        Political parties have used hate campaigns very effectively among members of
                                        similar race, religion, linguistics or caste. You just need to signal danger,
                                        the tribe get's together for defense. With the advent of social media, right
                                        wing politics has been on the rise as homogeneous groups have started coming
                                        together faster than ever.</p>
                                    <h5>How smart is Facebook really?</h5>
                                    <p>
                                        Social media companies may be very powerful, However even they have not
                                        succeeded in creating machines with analytical abilities of a 1 week old human
                                        baby - At least not yet. These companies are struggling to understand human
                                        emotions well enough. When you post something, these companies calculate the
                                        likability of your post using certain heuristics. Best internet Marketing
                                        companies still haven't succeeded in creating software that can read a post and
                                        judge to a reasonable accuracy whether people will like it or not.




                                        How to Trick People To engage</p>
                                    <h5>Digital Marketing - The winner gets all the traffic</h5>
                                    <p>The more engaging your content is, the more people will see your post. Social
                                        media platforms get paid for the advertisements shown on your news feed. They
                                        know you don't like the advertisements - so they put a lot of effort into giving
                                        you the most interesting content.If you somehow succeed in convincing the
                                        algorithm that you have an interesting content, half your work is done.

                                        So, the trick is to understand how they do a reasonably good job ? Most
                                        algorithms depend on engagement metrics -the proportion of reactions per persons
                                        reached is a clear measurement of engagement. Higher the engagement, higher the
                                        chances that more and more people see your post.

                                        <b> Post Engagement = Total Number of reactions / Total Number of People
                                            reached.</b>
                                    </p>
                                    <h5>How to Trick People To engage
                                    </h5>
                                    <div class="thumb">
                                        <a href="#"><img src="/images/brain-3-1536x706.jpg" style="width: 100%;"
                                                alt="Thumb"></a>
                                    </div>

                                    <h3>1.Be divisive</h3>
                                    <p>
                                        If you can get people to talk to each other on your comment box, it does magic
                                        for your post. It is not much difficult to do this, considering people can
                                        hardly agree with each other on many subjects. For e.g., imagine you are an
                                        actress - your fans can have very different opinions about your opinions, even
                                        clothes. Some will criticize and others will appreciate the same post. The more
                                        divisive the opinion, more chances of discussions. Donald Trump has one of the
                                        most engaging social media handles owing to divisive posts. This has helped his
                                        campaign very well.


                                    </p>
                                    <h3>2.Surprise Them !</h3>
                                    <p>Just like the many hues in a rainbow, human beings are capable of a wide range
                                        of emotions. Take for instance, surprise - this comes in several flavors , Awe,
                                        Shock, Dismay, Disillusion, Perplex, Astonishment, Eager, Energetic and maybe
                                        many more. Awe - Among the wide variety of emotions, awe is one emotion
                                        guaranteed to generate shares. If they think what you posted is awesome, they
                                        impulsively share your post. They may not even read the complete post before
                                        sharing.</p>
                                    <h3>3.Make them happy</h3>
                                    <p>
                                        Easiest way to make people happy is with a joke. It doesn't even have to be
                                        original. If your post makes people laugh, it is very likely that they may share
                                        it with their friends. People like to laugh together than alone.

                                        Happiness varies from just being playful to feeling optimistic. You share jokes
                                        because you are feeling playful, you share an inspiring post because you are
                                        feeling optimistic. Optimism is much more powerful emotion than playfulness,
                                        however it is much easier to joke than to inspire.



                                    </p>
                                    <h3>4.Make people angry</h3>
                                    <p>
                                        If your post makes people angry, they are very likely to respond by commenting,
                                        disliking, and sharing. Most viral posts have compounded anger through social
                                        media. Sometimes, posts have created and fueled riots and serious violence in
                                        the real world.

                                        You may feel Betrayed, resentful, disrespected, ridiculed, indignant, violated,
                                        furious, jealous, provoked, hostile, infuriated, annoyed, withdrawn, numb,
                                        skeptical or dismissive based on your exact state of mind. The more extreme the
                                        anger, more likely is your engagement. If you are dismissive, you are less
                                        likely to be engaged compared to when you feel betrayed.

                                        It doesn't take a PhD in psychology to know that it is far easier to make
                                        someone angry than to inspire him. If both emotions are equally effective, what
                                        tactic will a social media marketer use more often ? It's anyone's guess.</p>
                                    <h3>5.Scare them</h3>
                                    <p>
                                        Fear of the predator is one of the basic survival mechanisms hardwired into our
                                        psyche. If you are scared, all you can think about is the worst case scenario.
                                        Scaring people is one of the most powerful calls to action known to men. If you
                                        really want to know how fast you can run, try running in front of a hungry
                                        tiger.

                                        Fear of danger can motivate people to share your posts with their friends almost
                                        automatically.


                                    </p>
                                    <h3>6. Don't make them Sad</h3>
                                    <p>There are other emotions which help you get shares, none is as powerful as awe.
                                        Sadness is the least effective among these as sadness very rarely is a
                                        successful call for action. Extreme sadness makes people depressed and in some
                                        rare conditions even unable to move. Not a good idea if you want them to visit a
                                        website, or fill a form.</p>
                                    <h3>Critical Mass - The secret Ingredient of Viral digital Marketing</h3>
                                    <p>
                                        What does a nuclear bomb, a viral post and an epidemic have in common ? How is a
                                        nuclear bomb different from a conventional bomb ? Similarly how is an epidemic
                                        different from a normal contagious illness ? Difference can be summarized as
                                        linear progression versus arithmetic progression in mathematical terms. In other
                                        words, a normal post has linear growth, a viral post grows exponentially. Viral
                                        posts explode like an atom bomb.

                                        At least some of us have tried to create a viral post with all our strength, but
                                        miserably failed. At times, the post doesn't have the merits of high engagement,
                                        but most of the time, even a very engaging post fails to go viral.The best posts
                                        fail to catchup because they are not able to sustain interest for sufficient
                                        time.

                                        The phenomena is just like when the best actors fail to become stars, companies
                                        with great products go bust and bankrupt , and kids with great potential never
                                        make the mark. Key is to be at the right place at the right time. Your post
                                        needs to reach a sufficiently large number of people for it to sustain and
                                        spread out like a wild fire.

                                        One easy way to achieve this is to get influencer support. If a film star or a
                                        politician shares your post, it can achieve critical mass and get viral. If you
                                        have enough money, you can pay internet companies and reach out to a wide
                                        audience.Now, paid traffic is getting very expensive fewer and fewer people will
                                        be able to do this.

                                        Not surprisingly, influencer traffic reduces revenues for the social media
                                        company, soon you will find that fewer and fewer influencers will succeed in
                                        generating this critical mass.</p>

                                       <h3> How does fear dominate all other emotions ?</h3><div class="thumb">
                                        <a href="#"><img src="/images/fear-4-1024x681-1.jpg" style="width: 100%;"
                                                alt="Thumb"></a>
                                    </div>

                                      
                                    <p>There are two kinds of fears, one real and the other one outright stupid. Both
                                        are powerful drivers in internet and social media. Let's first look at real
                                        fear.

                                        Let's imagine two scenarios - One in which you discovered some news about
                                        certain individual or company causing grave danger to kids. How likely are you
                                        to share the information publically using your social media profile ? You want
                                        to share the news with family, friends, colleagues and community members. In a
                                        second scenario, a college friend shared a very sensous photograph. How likely
                                        are you to share the photograph publically ? In the second scenario, sharing is
                                        mostly in private groups. If you are a celebrity, you may share it with your
                                        close friends, but you are very unlikely to publish that photograph in their own
                                        social media handle.

                                        Even if you are the maverik and publish such content in your social media
                                        profile, your friends from opposite sex are unlikely to share the same
                                        enthusiasm as your buddies. Certainly, your parents are not going to share the
                                        same with their friends.The lesser people share your post, lower proliferation,
                                        lessens the likelyhood of the post becoming viral. Spreading irrational fear is
                                        much more socially acceptable compared to having inappropriate fun.

                                        Coming to the irrational fear - FOMO - Fear of missing out. You are always
                                        looking for new emails , new messages and new comments every chance you get.
                                        This is the goldmine internet companies have discovered in the last decade. You
                                        just don't want to miss out on the fun everyone else is having. It's hard to
                                        sustain anyone's attention for more than 5 minutes before them checking their
                                        mobile phones these days.

                                        As you are looking into your news feed missing out on the fun in the real world,
                                        advertisers are busily bidding for your attention to tell you what to buy. There
                                        are thousands of engineers burning midnight oil to keep you one second longer in
                                        the feed. That one second means one more dollar from the advertiser. As they
                                        say, when the product is free, you are the product. Facebook is selling your
                                        precious time to the highest bidder ! How did we get here ?

                                        Some undrestanding of tricks employed by marketing companies will help us in
                                        separating propaganda from truth in the internet. It will help us make the best
                                        of the only one resource money can not buy - our time !</p>
                                    


                                    


                                   

