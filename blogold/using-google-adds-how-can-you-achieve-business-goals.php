                                    <h2>How do Google Ads function? </h2>
                                    <br>
                                    <p>In order to display their advertising to potential customers, advertisers use
                                        Google AdWords' auction-based system to bid on certain keywords and target
                                        markets. Google conducts an auction to choose which advertisements will appear
                                        and in what order when a user searches for a keyword that an advertiser has
                                        placed a bid on.
                                        With Google AdWords, there are three primary price structures:<br><br>
                                       <b>Cost per Click (CPC)-</b>
