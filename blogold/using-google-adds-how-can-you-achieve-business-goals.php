                                        <b>Cost Per Thousand Impressions (CPM)-</b>
                                        <br><br> Regardless of whether a person clicks on the advertisement, advertisers
                                        get charged every 1,000 times their ad is shown. For display and video
                                        advertisements, this pricing strategy is frequently employed.<br><br>
                                       <b>Cost Per Engagement (CPE)-</b>
                                        <br><br> When a user interacts with an advertisement, such as by viewing a video
                                        ad for a certain period of time or by clicking on an expanded ad, advertisers
                                        get compensated.<br><br>
