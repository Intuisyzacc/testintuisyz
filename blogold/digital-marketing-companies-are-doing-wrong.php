                                    <p>My digital marketing journey started 5 years ago, before that,I was a programmer.<br>

                                        I started marketing feature films in social media.My initial efforts never
                                        succeeded in bringing people into the movie screens.Now everything seems very
                                        obvious, at that time, it didn’t occur to me that my efforts were not reaching
                                        sufficient number of people.<br>

                                        I succeeded the second time,I was able to reach out to a large number of people
                                        this time.I used facebook advertising, which was very cheap 4 years ago.This
                                        time again,I failed.Very few turned up at the theatre.<br>

                                        It is at this point,I started thinking like a salesman than a programmer.Digital
                                        marketing is not like java where the language obeys your command.Human beings
                                        are very complex.They will always find a way to NOT obey your command.<br>

                                        I started to think about the people who turn up a the movies on the releasing
                                        day.What are their motivations ? How can I convince them to buy tickets?At this
                                        point I realised it is important to match the product with the buyer.It is
                                        pointless to market a film to people more than 35 years if you want them to turn
                                        up on the first day.Similarly, women are better targeted from the 2nd week
                                        unless your movie can generate huge amount of hype.By the way, no need to call
                                        me a sexist

                                        yet, in India, women don’t visit theaters often, unless it is playing a very
                                        popular movie.<br>

                                        As I started marketing products ranging from websites, apartments to
                                        medicines,It became very obvious that understanding of human nature and behavior
                                        is the most important skill in a digital marketing professional’s kitty.In a
                                        perfect world, you would be able to reach out to only those people who are sure
                                        to purchase your product through various targeting techniques.In a perfect
                                        world, advertising expenses will be negligible.Nobody has to teach anybody there
                                        is no such thing as a perfect world.

                                        Targeting methods cost available in all digital advertising platforms are
                                        inversely related to ease of use.The more obvious your targeting, higher the
                                        price.For e.g.,it will cost you higher to target wealthy people.Everyone wants
                                        the rich to see their advertisements because they have the money to buy the
                                        service if they like it.<br>

                                        Now, let’s assume that you have successfully targeted your potential customers
                                        at a reasonable price,How do you convince them to purchase?
                                        <br>
                                        Most digital marketing companies pay little or no attention to user experience
                                        design.A good user experience will make it easier for the potential customers to
                                        decide whether they want to purchase from you or your competitor.The more
                                        difficult you make this decision, the user is more likely to give up.He may go
                                        to the competitor’s website and purchase their service even when your products
                                        may have been a better choice.<br>

                                        Web designers need to be careful about the aesthetics, but it doesn’t end
                                        there.Most of the time, a simple elegant easy to navigate website will win over
                                        an artistic yet difficult to navigate website. Make it a pleasure to surf your
                                        website, you will be singing all the way to the bank !
                                    </p>


