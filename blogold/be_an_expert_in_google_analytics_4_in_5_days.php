                                            </p>
                                            <p>
                                                <b>What is the difference between a user property and an event
                                                    parameter?</b><br>
                                                User Properties are similar to Custom Event Parameters in that
                                                they both
                                                offer context to an event, allowing you to better understand the
                                                nature
                                                of
                                                each event. User Properties, on the other hand, differ from
                                                Custom Event
                                                Parameters in the following ways:
                                            </p>
                                            <ul style="padding-left:20px;">
                                                <li style="list-style:square;">
                                                    <p>
                                                        You set User Property values once (using the setUserProperty
                                                        API), and
                                                        they
                                                        remain throughout the app's life cycle.
                                                    </p>
                                                </li>
                                                <li style="list-style:square;">
                                                    <p>
                                                        Once established as a User Property value, it is immediately
                                                        connected
                                                        with
                                                        every subsequent event that is logged.
                                                    </p>
                                                </li>
                                                <li style="list-style:square;">
                                                    <p>
                                                        You can utilize the User Property to filter all of your
                                                        reports if you
                                                        register it on the Analytics "User Properties" page.
                                                    </p>
                                                </li>
                                            </ul>
