                                    <blockquote>
                                        In Start Your Own e-Business, the staff at Entrepreneur Press and writer Rich
                                        Mintzer explain how to build a dotcom business that will succeed. In this book,
                                        you’ll find recipes for success, road maps that pinpoint the hazards and dozens
                                        of interviews with dotcom entrepreneurs who’ve proved they’ve got what it takes
                                        to survive in this sometimes fickle marketplace. In this edited excerpt, the
                                        authors discuss the 10 things you should never do when it comes to designing
                                        your e-business site.
                                    </blockquote>
                                    <p>
                                        This article could probably be called the 100 most deadly mistakes in
                                        website design–there are so many goofs site builders make–but let’s narrow the
                                        focus to the most disastrous 10. Avoid these gaffes, and your site will be far
                                        better than much of the competition.
                                    </p>
                                    <h3>1. Disabling the back button.</h3>

                                    <p> Evil site authors long ago figured out how to break a
                                        browser’s back button so that when a user pushes it, one of several undesired
                                        things
                                        happen: There’s an immediate redirect to an unwanted location, the browser stays
                                        put
                                        because the “back” button has been deactivated, or a new window pops up and
                                        overtakes the screen. Our advice: Never do it. All that’s accomplished is that
                                        viewers get annoyed and do not return to your site.</p>
                                    <h3>2. Opening new windows.</h3>

                                    <p>Once upon a time, using multiple new frames to display content as a user clicked
                                        through a site was cool–a new thing in web design. Now it only annoys viewers
                                        because it ties up system resources, slows computer response and generally
                                        complicates a visitor’s experience. Sure, it’s easy to use this tool. But don’t.
                                        With tabbed browsing common in browsers like Firefox, users who wish to open
                                        links in new tabs can do so if they wish.



                                    </p>
                                    <h3>3. Broken links.</h3>

                                    <p> If
                                        you’re selling, you need to offer viewers multiple ways to contact you. The
                                        smartest route is to put up a “Contact Us” link that leads to complete
                                        info–mailing address, phone and email address. That link should be on each and
                                        every page of your website. Even if nobody ever calls, the very presence of this
                                        information adds real-world legitimacy and transparency to your site and
                                        comforts some viewers.</p>
                                    <h3>4. Bad links–</h3>

                                    <p> hyperlinks that do nothing when clicked or lead to “404”
                                        error pages–are the bane of any web surfer. Test your site–and do it weekly–to
                                        ensure that all links work as promised. Include a “Contact the Webmaster” link
                                        in your site’s footer so users can quickly let you know if they find a broken
                                        link or other mistake on your site–and fix those errors immediately.</p>
                                    <h3>5. Slow server times.</h3>

                                    <p>Slow load times are inexcusable with professional sites — it’s
                                        an invitation to the visitor to click away. What’s slow? A recent study by
                                        Akamai Technologies, commissioned through Jupiter Research, showed that online
                                        shoppers, on average, will wait only four seconds for a site to load before
                                        clicking away. If your site is loading significantly slower than this, put it on
                                        a diet–images may be too large or special add-ons, like a Flash introduction,
                                        may be slowing things down.</p>
                                    <h3>6. Outdated information.</h3>

                                    <p>Again, there’s no excuse, but it’s amazing how many sites
                                        include old, dated content. Make sure to keep your site fresh and updated daily
                                        for best results. You can’t afford the loss of credibility that can come from
                                        having dated content. Also, make sure your content is accurate, and if you
                                        should find a single error, fix it immediately.



                                    </p>
                                    <h3>7. Too many font styles and colors.–</h3>

                                    <p> Pages ought to present a unified, consistent
                                        look, but novice site builders–entranced by having hundreds of fonts at their
                                        fingertips, plus dozens of colors–frequently turn their pages into a garish
                                        mishmash. Use two or three fonts and colors per page, maximum. The idea is to
                                        reassure viewers of your solidity and stability, not to convince them you’re
                                        wildly artistic. Also remember to make sure your font and colors look good on
                                        all possible devices from desktops to tablets to iPhones, etc.</p>
                                    <h3>8. Orphan pages.</h3>

                                    <p> Memorize this: Every page in your site needs a readily seen link
                                        back to the home page. Why? Sometimes users will forward a URL to friends, who
                                        may visit and may want more information. But if the page they get is a dead end,
                                        forget it. Always put a link to “Home” on every page, and make your site logo
                                        (usually found near the top left side of the screen) link back to your home
                                        page–that will quickly solve this problem.</p>
                                    <h3>9. Orphan pages.</h3>

                                    <p> Memorize this: Every page in your site needs a readily seen link
                                        back to the home page. Why? Sometimes users will forward a URL to friends, who
                                        may visit and may want more information. But if the page they get is a dead end,
                                        forget it. Always put a link to “Home” on every page, and make your site logo
                                        (usually found near the top left side of the screen) link back to your home
                                        page–that will quickly solve this problem.</p>
                                    <h3>10. Failing to link with your social network sites.</h3>

                                    <p> Most businesses have their
                                        own Facebook pages, others use Pinterest with boards full of photos, while some
                                        broadcast their latest activities on Twitter. The point is that social media is
                                        here to stay and businesses are benefitting from having a presence in it.
                                        Forgetting to link to your
                                        social media platforms is a big no-no. People should be able to go from one to
                                        the other effortlessly. Using social media to market your business and drive
                                        customers to your website will only work if you make it easy for users to move
                                        from one to the other. If done right, you can drive a lot more traffic to your
                                        site.</p>


