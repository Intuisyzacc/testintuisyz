                                            <li><h5 style="color:#000000"><b>1. The Innocent:</b></h5> The brand that believes in a better world, like the "Superman" of brands, is always virtuous and pure.

                                            </li><br>
                                            <li><h5 style="color:#000000"><b>2. The Everyman:</b></h5>  The "Spider-Man" of brands, relatable and down-to-earth, representing the common person's values.
