                                    <h3> Why is responsive design important..?
                                    </h3>

                                    <p>

                                        The concept of separate designs for mobile and desktop is now replaced with
                                        intuitive responsive designs which is compatible for all devices and thereby we
                                        can save 45% of time, cost and effort of web development. If the site is not
                                        responsive, then the readability of the site is different in desktop and mobile.
                                        If the site is responsive we can see the normal images (like desktop) without
                                        any failure.

                                        A major key to responsive web design is knowing your audience and what device
                                        they are using to look at your website. How much of your current traffic is
                                        desktop vs. tablet vs. mobile? Close to 56% of traffic in US websites is
                                        currently from mobile devices. Nowadays there are around 2.6 billion smartphone
