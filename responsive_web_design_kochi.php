                                    <h3><br> Media Screens


                                    </h3>


                                    <p>


                                        A responsive website design have different media screens for various browser
                                        widths. They are as follows,
                                        <br>
                                        // Extra small devices (portrait phones, less than 544 px)<br>
                                        @media (max-width: 544 px) { … }<br>
                                        // Small devices (landscape phones, 544 px and up)<br>
                                        @media (min-width: 544 px) { … }<br>
                                        // Medium devices (tablets, 768px and up)<br>
                                        @media (min-width: 768px) { … }<br>
                                        // Large devices (desktops, 992px and up)<br>
                                        @media (min-width: 992px) { … }<br>
                                        // Extra large devices (large desktops, 1200px and up)<br>
                                        @media (min-width: 1200px) { … }<br>
                                        How to Make a Mobile-Friendly Website : Responsive Design in CSS3 and html5
                                        In responsive design, we will present the same web page that desktop computer
                                        users see to your mobile audience. Only the CSS3, are totally different. That
                                        is, browsers on desktop or laptop computers can render the page using one set of
                                        CSS directions, whereas those on mobile phones another.



                                    </p>
                                    <h3> Benefits of a Responsive Web Design
                                    </h3>
                                    <h5>Flexibility</h5>
                                    <p>

                                        Your content is flexible enough to move seamlessly from one screen to next
                                        screen with responsive design. This implies that users have the flexibility to
                                        access your website on any of their devices without distorted content.

                                    </p>
                                    <h5> Easy To Manage</h5>
                                    <p>


                                        When you have different website for mobile and desktop, you also need two
                                        different content strategies, design teams and SEO campaigns. It is very
                                        complicated. Responsive design has a single foundation which will run on an
                                        equivalent methods and campaigns, which means there’s so much less work
                                        concerned for you.

                                    </p>
                                    <h5> Cost Effective


                                    </h5>
                                    <p>

                                        Responsive web design can drastically cut down on development cost and time.
                                        Instead of spending the time and resources constructing a separate mobile
                                        website in addition to your traditional desktop website, the responsive design
                                        approach permits you to optimize your business for all the devices with one
                                        website. One website costs less than two and therefore the savings is
                                        substantial.

                                    </p>
                                    <h5> Improved User Experience</h5>
                                    <p>


                                        Content is the king in the internet marketing realm, however if your users
                                        cannot access it with ease. If you create your website responsive, the content
                                        can come back to the page quickly and while not distortion. There is no manual
                                        resizing necessary to get it to fit; the responsive web design takes care of
                                        client needs for you.

                                    </p>
                                    <h5> Google Recommended</h5>
                                    <p>



                                        Usually, once google recommends one thing to the various internet marketers that
                                        use their search engines, they must listen. Besides the actual fact that Google
                                        owns sixty seven percent of the search market, they’re conjointly dedicated to
                                        the user expertise. They know from expertise that users love responsive design,
                                        thus if they tell you that you just have to be compelled to build your website
                                        responsive, that is what you must do.



                                    </p>

                                    <h5>Avoided Duplicate Content
                                    </h5>
                                    <p>

                                        The more newer quality update is panda update in google’s search ranking
                                        algorithm, duplicate content can affect your rankings. Different sites for
                                        desktop and mobile puts you in danger for running duplicate content on the web.
                                        Currently that google is as well as mobile-friendly sites into their rankings,
                                        content printed on the mobile web crosses over together with your desktop
                                        content, which means that you simply can be business duplicated content while
                                        not realizing it.

                                    </p>
                                    <h5> More Efficient Site Management</h5>
                                    <p>


                                        The management of your responsive website can price a lot of less and take a lot
                                        of less time than the management of two different sites. There is less content
                                        to manage and you will access all information one interface instead of coping
                                        with the complexities of 2. It is possible to manage different websites for
                                        multiple devices, but it will take more money and time. The advantages of going
                                        responsive are clear.
