                    location.reload();

                    // Reload captcha for the next submission
                    reloadCaptcha();
                },
                error: function () {
                    alert("Form submission failed. Please try again.");
                    // Reload captcha for another attempt
                    reloadCaptcha();
                }
            });
        } else {
            // Display an error message for an invalid captcha
            alert("Invalid captcha. Please try again.");

            // Reset the captcha input field
            captchaInput.value = "";

            // Reload captcha for another attempt
            reloadCaptcha();
        }
    });

    function generateCaptcha() {
        var characters = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789';
        var captchaString = '';
        for (var i = 0; i < 5; i++) {
            var randomIndex = Math.floor(Math.random() * characters.length);
            captchaString += characters.charAt(randomIndex);
        }
        return captchaString;
    }

    function reloadCaptcha() {
        var captchaCodeElement = document.getElementById("captchaCode");
        var captcha = generateCaptcha();
        captchaCodeElement.textContent = captcha;
    }

    function validateCaptcha() {
        var userEnteredCaptcha = captchaInput.value;
        var generatedCaptcha = document.getElementById("captchaCode").textContent;

        return userEnteredCaptcha === generatedCaptcha;
    }
});
