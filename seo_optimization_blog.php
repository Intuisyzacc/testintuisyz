                                    <p>
                                        Web design companies very often focus too much on the user experience, they
                                        forget arguably most important customer they should satisfy – the google robots.
                                        Search Engine Optimization or SEO is the process used to improve traffic to a
                                        website by getting a high-rank placement within the Search Engine Results Page
                                        like Google, Yahoo, Bing, Yandex etc. SEO has two techniques, they are On Page
                                        SEO and Off page SEO.
                                    </p>
                                    <h3> On Page SEO</h3>
                                    <p>
                                        Onpage SEO refers to all the things that we do within individual web pages like
                                        content, keyword stuffing, title, description, image optimization, internal
                                        linking etc. It is the practice of optimizing our website (both the content and
                                        HTML source code of a page that can be optimized) in order to rank higher and
                                        get relevant traffic in search engines. Overall the on-page technique refers
                                        building your website to be search engine friendly.</p>

                                    <h3> Off Page SEO</h3>
                                    <p>Off page SEO is an important part of search engine optimization. It is the
                                        method to market your websites in search engines by performing some activities
                                        outside the web pages. It is used to make your website popular on the internet.
                                        If the website is popular on the internet, you will be able to get more
                                        visibility. Using the on-page SEO techniques, we are able to get visibility in
                                        search engines. Off-page techniques are used to improve the website’s position
                                        in search engine result pages. Back linking is the most popular off page SEO
                                        method. To build quality back links from different high domain authority
                                        websites, bookmark your web pages in social bookmarking sites and share content
                                        in social media sites</p>


